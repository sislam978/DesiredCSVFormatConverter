
public class VolatilityIndex {
	
	public Integer id;	
	public String historydatadate;
	public Double Open;
	public Double High;
	public Double Low;
	public Double Close;
	public Double AdjClose;
	
	public Integer getId() {
		return id;
	}
	public void setId(Integer id) {
		this.id = id;
	}
	public String getHistorydatadate() {
		return historydatadate;
	}
	public void setHistorydatadate(String historydatadate) {
		this.historydatadate = historydatadate;
	}
	public Double getOpen() {
		return Open;
	}
	public void setOpen(Double open) {
		Open = open;
	}
	public Double getHigh() {
		return High;
	}
	public void setHigh(Double high) {
		High = high;
	}
	public Double getLow() {
		return Low;
	}
	public void setLow(Double low) {
		Low = low;
	}
	public Double getClose() {
		return Close;
	}
	public void setClose(Double close) {
		Close = close;
	}
	public Double getAdjClose() {
		return AdjClose;
	}
	public void setAdjClose(Double adjClose) {
		AdjClose = adjClose;
	}
	
	

}
